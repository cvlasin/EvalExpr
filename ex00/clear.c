#include "eval_expr.h"

void	clear(char *str)
{
	int i;
	int j;
	int ok;

	ok = 0;
	while (ok == 0)
	{
		i = 0;
		ok = 1;
		while (str[i] != '\0')
		{
			if (str[i] == ' ')
			{
				ok = 0;
				str[i] = str[i + 1];
				j = i + 1;
				while (str[j] != '\0')
				{
					str[j] = str[j + 1];
					j++;
				}
			}
			i++;
		}
	}
}
